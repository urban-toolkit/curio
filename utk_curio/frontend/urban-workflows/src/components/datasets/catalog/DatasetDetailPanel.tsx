import React, { useState } from "react";
import {
  DATASET_FORMAT_LABEL,
  DatasetCatalogItem,
  datasetProvenanceLabel,
} from "../../../services/datasetCatalog";
import {
  formatNodeTypeLabel,
  lineageUsageSummary,
  upstreamOriginCaption,
  useDatasetLineage,
  type DatasetLineage,
  type DatasetLineageNodeUsageRef,
  type LineageStatus,
} from "../../../services/datasetLineage";
import {
  datasetCount,
  datasetIconVariant,
  datasetInitials,
  formatBytes,
  formatClass,
  relativeTime,
} from "./datasetDetailHelpers";
import styles from "./DatasetDetailPanel.module.css";
import { DatasetSchemaPanel } from "./DatasetSchemaPanel";
import { DatasetTablePreview } from "./DatasetTablePreview";
import { useDatasetResolvedSchema } from "./useDatasetResolvedSchema";

const TABS = ["Overview", "Schema", "Table Preview", "Lineage"] as const;

const LINEAGE_STATUS_LABEL: Record<LineageStatus, string> = {
  active: "Active",
  stale: "Stale",
  missing: "Missing",
  unresolved: "Unresolved",
};

function statusPillClass(status: LineageStatus): string {
  switch (status) {
    case "active":
      return `${styles.statusPill} ${styles.statusActive}`;
    case "stale":
      return `${styles.statusPill} ${styles.statusStale}`;
    default:
      return `${styles.statusPill} ${styles.statusBroken}`;
  }
}

const LineageStatusPill: React.FC<{ status: LineageStatus }> = ({ status }) => (
  <span className={statusPillClass(status)}>{LINEAGE_STATUS_LABEL[status]}</span>
);

function usageDisplayName(usage: DatasetLineageNodeUsageRef): string {
  if (usage.nodeName) return usage.nodeName;
  return `Node ${usage.nodeId.slice(0, 8)}`;
}

const DownstreamUsageCards: React.FC<{
  usages: DatasetLineageNodeUsageRef[];
}> = ({ usages }) => (
  <>
    {usages.map((usage, index) => (
      <article className={styles.consumedCard} key={usage.nodeId}>
        <div
          className={
            usage.status === "active" || usage.status === "stale"
              ? index % 2 === 0
                ? styles.lineageCardAccentMuted
                : styles.lineageCardAccentBlue
              : styles.lineageCardAccentWarm
          }
        />
        <div>
          <strong>{usageDisplayName(usage)}</strong>
          {usage.nodeType ? (
            <span className={styles.lineageNodeBadgeMuted}>
              {formatNodeTypeLabel(usage.nodeType)}
            </span>
          ) : null}
          <span>
            {usage.status === "unresolved"
              ? "Reference not found in this dataflow"
              : usage.dataflowName
                ? `Dataflow: ${usage.dataflowName}`
                : "Current dataflow"}
          </span>
        </div>
        <LineageStatusPill status={usage.status} />
      </article>
    ))}
  </>
);

const UpstreamCards: React.FC<{
  dataset: DatasetCatalogItem;
  lineage: DatasetLineage;
}> = ({ dataset, lineage }) => {
  const generatingNode = lineage.upstream.generatingNode;
  return (
    <>
      <article className={styles.originCard}>
        <div className={styles.lineageCardAccentWarm} />
        <div>
          {generatingNode ? (
            <>
              <strong>
                Generated by{" "}
                {generatingNode.nodeName || `node ${generatingNode.nodeId.slice(0, 8)}`}
              </strong>
              {generatingNode.nodeType ? (
                <span className={styles.lineageNodeBadge}>
                  {formatNodeTypeLabel(generatingNode.nodeType)}
                </span>
              ) : null}
              <span>{upstreamOriginCaption(dataset)}</span>
            </>
          ) : (
            <>
              <strong>{lineage.upstream.originLabel}</strong>
              <span>{DATASET_FORMAT_LABEL[dataset.format]} dataset</span>
            </>
          )}
        </div>
      </article>
      {dataset.origin === "hub" || dataset.installed ? (
        <div className={styles.hubSourceCard}>
          <strong>Installed from Data Catalog</strong>
          <span>
            {dataset.sourceLabel || datasetProvenanceLabel(dataset.origin)} · v1
          </span>
        </div>
      ) : null}
    </>
  );
};

const LineageStates: React.FC<{
  lineage: DatasetLineage;
  canvasAvailable: boolean;
}> = ({ lineage, canvasAvailable }) => {
  if (lineage.status.hasUnresolvedReferences) {
    return (
      <p className={styles.lineagePartialNote}>
        Lineage is partially available. Some node or dataflow references could
        not be resolved.
      </p>
    );
  }
  if (!canvasAvailable) {
    return (
      <p className={styles.lineagePartialNote}>
        Lineage is computed from the open dataflow. Open this dataset from a
        dataflow to see live usage.
      </p>
    );
  }
  return null;
};

/** Expanded lineage view rendered in the center column for the Lineage tab. */
const LineageMainSection: React.FC<{
  dataset: DatasetCatalogItem;
  lineage: DatasetLineage;
  canvasAvailable: boolean;
}> = ({ dataset, lineage, canvasAvailable }) => {
  const { consumingNodes } = lineage.downstream;
  return (
    <div className={styles.lineageSection}>
      <div className={styles.lineageHeader}>
        <h2>
          Data Flows <span className={styles.liveBadge}>live</span>
        </h2>
        <p>Dataflows that generate or consume this dataset</p>
      </div>

      <LineageStates lineage={lineage} canvasAvailable={canvasAvailable} />

      <p className={styles.lineageLabel}>Generated by</p>
      <UpstreamCards dataset={dataset} lineage={lineage} />

      <p className={styles.lineageLabel}>Consumed by ({consumingNodes.length})</p>
      {consumingNodes.length > 0 ? (
        <DownstreamUsageCards usages={consumingNodes} />
      ) : (
        <p className={styles.lineageEmpty}>
          No nodes or dataflows are currently using this dataset.
        </p>
      )}
    </div>
  );
};

export interface DatasetDetailPanelProps {
  dataset: DatasetCatalogItem | null;
  loading?: boolean;
  error?: string | null;
  variant?: "page" | "modal";
  dataflowId?: string | null;
  liveOutputs?: Array<{ node_id: string; filename: string; data_type?: string }>;
  onBack?: () => void;
}

export const DatasetDetailPanel: React.FC<DatasetDetailPanelProps> = ({
  dataset,
  loading = false,
  error = null,
  variant = "modal",
  dataflowId = null,
  liveOutputs,
  onBack,
}) => {
  const [activeTab, setActiveTab] = useState<(typeof TABS)[number]>("Overview");
  // The standalone catalog page renders without a canvas, so live usage
  // cannot be resolved there and lineage is flagged as partial.
  const canvasAvailable = variant === "modal";
  const lineage = useDatasetLineage(dataset, { dataflowId, canvasAvailable });
  // Shared schema resolution so the header/info counts match the schema panel.
  const resolvedSchema = useDatasetResolvedSchema(dataset, dataflowId, liveOutputs);
  const fields = resolvedSchema.fields;
  const columnsLabel =
    fields.length > 0
      ? fields.length.toLocaleString()
      : resolvedSchema.fetching
        ? "…"
        : "Unknown";
  const rootClass = variant === "page" ? styles.pageRoot : styles.modalRoot;

  if (loading && !dataset) {
    return <p className={styles.loading}>Loading dataset...</p>;
  }

  if (error) {
    return <p className={styles.error}>{error}</p>;
  }

  if (!dataset || !lineage) {
    return <p className={styles.loading}>Dataset not found.</p>;
  }

  const iconVariant = datasetIconVariant(dataset);
  const countLabel = datasetCount(dataset);
  const tags = dataset.tags.length > 0 ? dataset.tags : [dataset.format];
  const { consumingNodes } = lineage.downstream;
  const showSchemaColumn = activeTab === "Overview";
  const bodyClass = showSchemaColumn
    ? styles.inspectorBody
    : `${styles.inspectorBody} ${styles.inspectorBodyTwoCol}`;

  return (
    <main className={`${styles.inspector} ${rootClass}`}>
      <div className={styles.pageHeader}>
        <div className={styles.breadcrumb}>
          {variant === "page" ? (
            <>
              <span>DATA CATALOG</span><span>/</span><span>INSTALLED DATASETS</span><span>/</span>
            </>
          ) : (
            <>
              <span>DATA CATALOG</span><span>/</span>
            </>
          )}
          <strong>{dataset.title}</strong>
        </div>

        {variant === "page" && onBack ? (
          <button className={styles.backButton} type="button" onClick={onBack}>Back</button>
        ) : null}

        <div className={styles.headerMain}>
          <div className={styles.titleBlock}>
            <span className={`${styles.datasetIcon} ${styles[`icon_${iconVariant}`]}`}>
              {datasetInitials(dataset.title)}
            </span>
            <div>
              <h1>{dataset.title}</h1>
              <div className={styles.inspectorMeta}>
                <span className={styles.installedBadge}>
                  {dataset.installed ? "Installed" : datasetProvenanceLabel(dataset.origin)}
                </span>
                <span className={formatClass(dataset.format, styles)}>{DATASET_FORMAT_LABEL[dataset.format]}</span>
                {countLabel ? <span>{countLabel}</span> : null}
                {fields.length > 0 ? (
                  <>
                    <span className={styles.metaDot} aria-hidden="true">·</span>
                    <span>{fields.length} columns</span>
                  </>
                ) : null}
                {formatBytes(dataset.sizeBytes) ? (
                  <>
                    <span className={styles.metaDot} aria-hidden="true">·</span>
                    <span>{formatBytes(dataset.sizeBytes)}</span>
                  </>
                ) : null}
                <span className={styles.metaDot} aria-hidden="true">·</span>
                <span>Updated {relativeTime(dataset.updatedAt)}</span>
              </div>
            </div>
          </div>
          <div className={styles.inspectorActions}>
            <button className={styles.publishButton} type="button">Publish to Catalog</button>
            <button className={styles.exportButton} type="button">Export</button>
          </div>
        </div>

        <nav className={styles.inspectorTabs} aria-label="Dataset detail sections">
          {TABS.map((tab) => (
            <button
              key={tab}
              type="button"
              className={tab === activeTab ? styles.inspectorTabActive : ""}
              onClick={() => setActiveTab(tab)}
            >
              {tab}
            </button>
          ))}
        </nav>
      </div>

      <div className={bodyClass}>
        {showSchemaColumn ? (
          <section className={styles.schemaColumn} aria-label="Schema">
            <DatasetSchemaPanel schema={resolvedSchema} />
          </section>
        ) : null}

        <section className={styles.centerColumn} aria-label="Dataset content">
          {activeTab === "Schema" ? (
            <DatasetSchemaPanel schema={resolvedSchema} />
          ) : activeTab === "Lineage" ? (
            <LineageMainSection
              dataset={dataset}
              lineage={lineage}
              canvasAvailable={canvasAvailable}
            />
          ) : (
            <div className={styles.previewSection}>
              <div className={styles.previewSubtab}>
                <span className={styles.previewSubtabActive}>Table Preview</span>
              </div>
              <DatasetTablePreview dataset={dataset} dataflowId={dataflowId} liveOutputs={liveOutputs} />
            </div>
          )}
        </section>

        <aside className={styles.infoColumn} aria-label="Dataset info">
          <div className={styles.infoHeader}>
            <h2>Dataset Info</h2>
          </div>

          <div className={styles.infoSection}>
            <p className={styles.infoSectionLabel}>General</p>
            <dl className={styles.infoRows}>
              <div><dt>Format</dt><dd>{DATASET_FORMAT_LABEL[dataset.format]}</dd></div>
              <div><dt>Total {dataset.featureCount != null ? "features" : "records"}</dt><dd>{(dataset.featureCount ?? dataset.rowCount)?.toLocaleString() || "Unknown"}</dd></div>
              <div><dt>Columns</dt><dd>{columnsLabel}</dd></div>
              <div><dt>File size</dt><dd>{formatBytes(dataset.sizeBytes) || "Unknown"}</dd></div>
              {dataset.schema?.crs ? (
                <div><dt>CRS</dt><dd>{dataset.schema.crs}</dd></div>
              ) : null}
              <div><dt>Availability</dt><dd><span className={styles.installedBadge}>{dataset.installed ? "Installed" : "Available"}</span></dd></div>
              <div><dt>Created</dt><dd>{new Date(dataset.updatedAt).toLocaleDateString(undefined, { month: "short", day: "numeric", hour: "2-digit", minute: "2-digit" })}</dd></div>
              <div><dt>Last updated</dt><dd>{relativeTime(dataset.updatedAt)}</dd></div>
            </dl>
          </div>

          <div className={styles.infoSection}>
            <p className={styles.infoSectionLabel}>
              Lineage <span className={styles.liveBadge}>live</span>
            </p>
            <p className={styles.lineageSummary}>
              {lineageUsageSummary(lineage.downstream)}
            </p>

            <LineageStates lineage={lineage} canvasAvailable={canvasAvailable} />

            <details className={styles.lineageGroup} open>
              <summary className={styles.lineageGroupSummary}>
                Downstream usage ({consumingNodes.length})
              </summary>
              {consumingNodes.length > 0 ? (
                <DownstreamUsageCards usages={consumingNodes} />
              ) : (
                <p className={styles.lineageEmpty}>
                  No nodes or dataflows are currently using this dataset.
                </p>
              )}
            </details>

            <details className={styles.lineageGroup} open>
              <summary className={styles.lineageGroupSummary}>Upstream</summary>
              <UpstreamCards dataset={dataset} lineage={lineage} />
            </details>
          </div>

          <div className={styles.infoSection}>
            <p className={styles.infoSectionLabel}>Tags</p>
            <div className={styles.tagList}>
              {tags.map((tag) => (
                <span className={styles.tagChip} key={tag}>{tag}</span>
              ))}
            </div>
          </div>
        </aside>
      </div>
    </main>
  );
};
